# Demonstrate about the string slicing and extracting the substring

# Original string

s = "Hello, World!"

# Print the original string

print("Original string:", s)

# Extract the substring from the 6th character to the 12th character    

substring = s[5:12]

# Print the extracted substring

print("Substring:", substring)

# Extract the substring from the 6th character to the end of the string

substring = s[5:]

# Print the extracted substring

print("Substring:", substring)

